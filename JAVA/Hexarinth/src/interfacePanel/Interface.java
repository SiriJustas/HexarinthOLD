    public void printComponent(Component component){
        PrinterJob pj = PrinterJob.getPrinterJob();
        pj.setJobName(" Print Component ");

        pj.setPrintable (new Printable() {    
          public int print(Graphics pg, PageFormat pf, int pageNum){
            if (pageNum > 0){
            return Printable.NO_SUCH_PAGE;
            }

            Graphics2D g2 = (Graphics2D) pg;
            g2.translate(pf.getImageableX(), pf.getImageableY());
            component.paint(g2);
            return Printable.PAGE_EXISTS;
          }
        });
        if (pj.printDialog() == false)
        return;

        try {
              pj.print();
        } catch (PrinterException ex) {
              // handle exception
        }
      }
    
    public void saveImage(String name,String type) {
                BufferedImage images = new BufferedImage(output.getHeight()-10, output.getWidth(), BufferedImage.TYPE_INT_RGB);
                Graphics2D gg = images.createGraphics();
                try {
                    gg.translate(0, 0);  // start point of region negated
                    output.paint(gg);
                } finally {
                    gg.dispose();
                }

		try{
			ImageIO.write(images, type, new File(name+"."+type));
		} catch (Exception e) {
			e.printStackTrace();
		}
	}
